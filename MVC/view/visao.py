
class Visao:
    """Visão para exibir insformações no console"""
    @staticmethod
    def exibir_detalhes_livro(titulo, autor):
        print(f"Livro: {titulo}, Autor: {autor}")
    
    @staticmethod
    def exibir_mensagem(mensagem):
        """Exibe uma mensagem qualquer"""
        print(mensagem)
    
    